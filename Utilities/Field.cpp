Field::Field() {
    grass = TextureManager::LoadTexture("../Assets/Terrain/Grass.png");
    grass_alt = TextureManager::LoadTexture("../Assets/Grass.png");
    grass_alt2 = TextureManager::LoadTexture("../Assets/Grass.png");
    loadMap(Terrain);

    src.x = src.y = 0;
    src.w = src.h =  64;
    dest.h = dest.w = 48;
    dest.x = 0;
    dest.y = 0;
}

Field::~Field() {}

void Field::loadMap(int arr[25][40]) {
    for (int row = 0; row < 25; ++row) {
        for (int col = 0; col < 40; ++col) {
            map[row][col] = arr[row][col];
        }
    }
}

void Field::drawMap() {
    int type = 0;
    for (int row = 0; row < 25; ++row) {
        for (int col = 0; col < 40; ++col) {
            type = map[row][col];
            dest.x = col*48;
            dest.y = row*48;
            switch (type) {
                case 0:
                    TextureManager::Draw(grass, src,dest);
                    break;
                case 1:
                    TextureManager::Draw(grass_alt, src,dest);
                    break;
                case 2:
                    TextureManager::Draw(grass_alt2, src,dest);
                    break;
                default:
                    break;
            }
        }
    }
}